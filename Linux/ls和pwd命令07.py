# 1、ls
    # ls：命令默认查找的是当前路径下的非隐藏的文件
    # ls -a：查看当前路径下的所有文件
    # ls -l:以长格式显示文件
    # ls -h：显示文件大小
    # ls -alh：以长格式显示当前路径下的所有文件和大小
    # ls home/jianghu ：查看home/jianghu路径下的非隐藏的文件
    # 正则表达式
        # 通配符* ls 2* ：查看当前路径下以2开头的文件，代表多个
        # 通配符？:代表一个
        # [1-5]

