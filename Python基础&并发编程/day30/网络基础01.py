# 8、TCP协议：是长链接的，可靠安全的、慢
# 全双工：双方都可以收发信息，TCP协议一定是全双工的。
# 首先：三次握手建立链接：首先客户端发送消息给服务端（是否可以发消息给客户端）、服务端发送同意信息并且发送（是否可以发消息给客户端）、客户端回复可以发送消息，这样三次握手就完成了。
# 其次：数据传输：
# 最后：断开链接，四次挥手，再见时可以先客户端发起，也可以服务端发起。1、假设服务端发起我们断开吧、2、客户端说可以，3、然后客户端说我们断开吧，4、服务端说可以。


# 9、UDP协议：是不可靠的，无链接、快
# 两个程序之间通过UDP协议链接时不需要先发确认链接消息。


# 10、互联网协议和OSI模型：
# 互联网协议按照功能不同分为OSI七层或TCP/IP五层或者TCP/IP四层
# 从上到下依次是：应用层、传输层、网络层、数据链路层、物理层

# 物理层：网线、网卡          第5步
# 数据链路层：通过ip地址取mac地址，arp协议          第4步
# 网络层：ip协议，给上一步的协议添加了ip信息          第3步
# 传输层：选择通信协议tcp/udp协议          第2步
# 应用层：编写代码          第1步
# 可以理解为一个包快递和拆快递的过程
